# Float --> numbers with decimal points. Example: 3.14
e = 1.5
d = 2.4
f = e+d #will add the value of e and d 
print(f) # prints the value f 

# complex: Represents complex numbers. 
z = 2 + 3j

# list: Represents lists (ordered, mutable sequences). 
my_list = [1, 2, 3]
print(my_list)

# tuple: Represents tuples (ordered, immutable sequences).
my_tuple = (1, 2, 3)
print(my_tuple)

# set: Represents sets (unordered collections of unique elements).
my_set = {1, 2, 3}
print(my_set)

# frozenset: Represents immutable sets. Example: 
my_frozenset = frozenset([1, 2, 3])
print(my_frozenset)





