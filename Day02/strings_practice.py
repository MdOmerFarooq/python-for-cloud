# Lets perform some operations using inbuilt functions to learn strings
text = ' i am a cloud/Devops Engineer '

# finding the Length of string 
print(len(text))

# modifiyng the string to lower case and upper case
print(text.upper())
print(text.lower())

# splitting the text based on "/" letter
print(text.split('/'))

# stripping the spaces from string 
print(text.strip())

