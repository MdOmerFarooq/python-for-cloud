
# Integer variables
num1 = 10
num2 = 5

# Integer Division
result11 = num1 // num2
print("Integer Division:", result11)

# Modulus (Remainder)
result22 = num1 % num2
print("Modulus (Remainder):", result22)

# Absolute Value
result33 = abs(-7)
print("Absolute Value:", result33)


# Float variables
num3 = 5.0
num4 = 2.5

# Basic Arithmetic
result1 = num3 + num4
print("Addition:", result1)

result2 = num3 - num4
print("Subtraction:", result2)

result3 = num3 * num4
print("Multiplication:", result3)

result4 = num3 / num4
print("Division:", result4)

# Rounding
result5 = round(3.14159265359, 2)  # Rounds to 2 decimal places
print("Rounded:", result5)