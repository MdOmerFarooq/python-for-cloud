
# This file is a module created to store functions

# funtion to add

def add(num1 , num2):             # def is a keyword used to define a funtion . 
    result = num1 + num2    # Result is a local variable which means you can use this inside the function only
    return result           # returns the value whenever funtion is called 

# funtion to Subtract

def sub(num1 , num2):
    result = num1 - num2    
    return result 

# funtion to multiply

def mul(num1 , num2):
    result = num1 * num2    
    return result 

# funtion to Divide

def div(num1 , num2):
    result = num1 / num2    
    return result 

