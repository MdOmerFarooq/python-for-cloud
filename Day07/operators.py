# lets learn some operators in python with practice .

# Arithmetic stuff
a = 15
b = 5

sum = a + b
diff = a - b
mul = a * b
div = a / b

print("Arithmetic results:")
print("Sum:", sum)
print("Difference:", diff)
print("Product:", mul)
print("Quotient:", div)
print() # new line for formatting

# Comparison operators 

print("a < b ?", a < b)
print("a > b ?", a > b)
print("a <= b ?", a <= b)
print("a >= b ?", a >= b)
print("a == b ?", a == b)
print("a != b ?", a != b)
print()

# Logical operators
x = True
y = False

print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
print("not y:", not y)
print()

# Assignment operators
total = 10
print(total)
total += 5
print(total)
total -= 3
print(total)
total *= 2
print(total)
total /= 4
print("Final total:", total)
